//Created a class to convert Kilometer to Miles

public class KilometerToMiles {
    public static void main(String[] args) {
        
	//Distance is given in kilometers as
	double kilometers = 10.8;

	//Distance converted in miles 
        double miles = kilometers * 1.6;

	//Displaying km converted into miles
        System.out.println("Distance in km is : "+ kilometers +", Distance in miles is: "+ miles);
    }
}