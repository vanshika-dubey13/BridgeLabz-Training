package com.scenariobased.markmate;

public class Student {
	//attributes
    int rollNo;
    String name;
    int total;
    double average;
    String grade;

    //constructor
    public Student(int rollNo, String name, int total, double average, String grade) {
        this.rollNo = rollNo;
        this.name = name;
        this.total = total;
        this.average = average;
        this.grade = grade;
    }
}
